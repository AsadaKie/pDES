package org.pdes.rcp.core;

import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.ui.console.ConsolePlugin;
import org.eclipse.ui.console.IConsole;
import org.eclipse.ui.console.MessageConsole;
import org.eclipse.ui.console.MessageConsoleStream;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;
/**
 * This class is the activator class for start up this software.<br>
 * @author Taiga Mitsuyuki <mitsuyuki@sys.t.u-tokyo.ac.jp>
 *
 */
public class Activator extends AbstractUIPlugin {
	
	// The plug-in ID
	public static final String PLUGIN_ID = "pDES"; //$NON-NLS-1$

	// The shared instance
	private static Activator plugin;
	
	//Consoleç”¨
	private MessageConsole myConsole; 
	private MessageConsoleStream msgStream;
	
	/**
	 * This is the constructor.<br>
	 */
	public Activator() {
		myConsole = new MessageConsole("Console", null); 
		ConsolePlugin.getDefault().getConsoleManager().addConsoles(new IConsole[] { myConsole }); 
		msgStream = myConsole.newMessageStream();
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Get the shared instance
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

	/**
	 * Returns an image descriptor for the image file at the given
	 * plug-in relative path
	 *
	 * @param path the path
	 * @return the image descriptor
	 */
	public static ImageDescriptor getImageDescriptor(String path) {
		return imageDescriptorFromPlugin(PLUGIN_ID, path);
	}
	
	/**
	 * Get the instance of console view.
	 * @return the myConsole
	 */
	public MessageConsole getMyConsole() {
		return myConsole;
	}

	/**
	 * Get the instance of console stream.
	 * @return the msgStream
	 */
	public MessageConsoleStream getMsgStream() {
		return msgStream;
	}

}
